#რიცხვის გამოცნობის თამაში
#სთხოვს მომხმარებელს მოიფიქროს რიცხვი 1-დან 100-მდე და პროგრამა შეეცდება მის გამოცნობას.
##პროგრამა არაერთხელ ცდის გამოცნობას, თუ გამოცნობილი რიცხვი არის მაღალი, მაშინ უნდა გამოიტანოს  'მაღალი'
##თუ გამოცნობილი რიცხვი ძალიან დაბალია, მაშინ უნდა გამოიტანოს  "დაბალი".
#თუ გამოცნობილი რიცხვი სწორია, მაშინ უნდა გამოიტანოს  "სწორია".
#მთლიანობაში უნდა იყვეს 3 მცდელობა.
#თამაში მთავრდება, როდესაც პროგრამა გამოიცნობს სწორ რიცხვს (break)

import random

def guess_number():
    print("გთხოვთ, მოიფიქროთ რიცხვი 1-დან 100-მდე.")
    print("მე ვეცდები გამოვიცნო ის.")

    # დადგინდა მცდელობების რაოდენობა
    attempts = 3

    # თამაშის ლოგიკა
    for attempt in range(attempts):
        guess = random.randint(1, 100)  # პროგრამა გამოიცნობს შემთხვევით რიცხვს

        print(f"მცდელობა {attempt + 1}: მე ვეწვრილები რიცხვს {guess}")

        # მომხმარებელს უნდა უთხრას, თუ რიცხვი მაღალი, დაბალი ან სწორია
        feedback = input("თქვენი რიცხვი უფრო მაღალია, დაბალი თუ სწორია? (მაღალი/დაბალი/სწორია): ").strip().lower()

        if feedback == 'სწორია':
            print(f"დიდი მადლობა! მე შევამჩნიე თქვენი რიცხვი {guess}.")
            break
        elif feedback == 'მაღალი':
            print("ჩემი რიცხვი მაღალი იყო, ვცდილობ უფრო დაბალს.")
        elif feedback == 'დაბალი':
            print("ჩემი რიცხვი დაბალი იყო, ვცდილობ უფრო მაღალს.")
        else:
            print("არასწორი პასუხი. გთხოვთ, გამოასწოროთ პასუხი.")
            break
    else:
        print("სამწუხაროდ, მე ვერ შევამჩნიე თქვენი რიცხვი 3 მცდელობის დროს.")

# გაშვება თამაშის ფუნქციის
guess_number()

