
#ახსენით კომენტარის სახით თუ რა არის page section/გვერდის სექციები

#(Header) - ეს არის ვებგვერდის ზედა ნაწილი, სადაც ხშირად განთავსებულია ლოგო, მენიუ, ძებნის ველები და სხვა მნიშვნელოვანი ელემენტები.
#ნავიგაცია (Navigation) - აღნიშნავს მენიუს, რომელიც საშუალებას იძლევა მომხმარებელს სწრაფად გადავიდეს სხვადასხვა გვერდებზე.
#მთავარი შინაარსი (Main Content) - გვერდის ცენტრალური ნაწილი, სადაც განთავსებულია მთავარი ინფორმაცია, სტატია ან პროდუქტის აღწერა.
#ფუტერი (Footer) - გვერდის ქვედა ნაწილი, სადაც შეიძლება იყოს კავშირები, სამართლებრივი ინფორმაცია, სოციალური მედია ლინკები და სხვა მსგავსი ელემენტები.



#header სექცია წარმოადგენს ვებსაიტის მთავარი ზედა ნაწილი, სადაც ჩვეულებრივ ხვდებიან ლოგო, ნავიგაცია და სხვა მნიშვნელოვანი ინფორმაციის ელემენტები. 
#აქ შეიძლება იყოს:
#ლოგო (რეგულარულად <img> ელემენტი)
#მენიუ (ამოირჩევა <nav> ელემენტი)
#გვერდის სათაური (<h1>)
#ძებნის ფორმა (<form>, <input> და სხვა)

#nav სექცია შეიცავს ნავიგაციის ელემენტებს, რომლებიც მოიცავს ლინკებს ან მენიუს, რათა მომხმარებლებს გაუხსნათ სხვა გვერდები ან სექციები.
#აქ შეიძლება იყოს:
#ლინკების სიები (<ul>, <ol>, <li>, <a>)
#ნავიგაციის ელემენტები, რომლებიც შეიძლება იყოს როგორც ფორმატირებული, ასევე სუფთა ტექსტური ლინკები

#main სექცია წარმოადგენს მთავარ კონტენტს და იქ უნდა შეიცავდეს ძირითად ინფორმაციას, რომელიც არის ვებგვერდის შინაარსი.
#აქ შეიძლება იყოს:
#პოსტი (მაგალითად, <article> ან <section>)
#ტექსტი (როგორიცაა <p>, <h2>, <h3>)
#გამოსახულებები (<img>)
#ჩამონათვალები (<ul>, <ol>, <li>)
#სხვა ელემენტები, რომლებიც ასახავენ გვერდის მთავარ შინაარს სადაც განთავსდება სხვადასხვა ინფორმაცია.

#ახსენით კიდევ სხვა სექციები, რომლებიცაა: section, article, div, footer და aside



#article სექცია წარმოადგენს ინდივიდუალურ სტატიას, პოსტს ან სხვა განაწესს, რომელსაც აქვს თავისი დამოუკიდებელი მნიშვნელობა.
#ეს შეიძლება იყოს:
#სტატია (<h2>, <p>, <img>, )
#პოსტები, რომლებიც შეიძლება გამოიცვალოს და ჰქონდეს კომენტარები




#div სექცია გამოიყენება როგორც კონტეინერი სხვა ელემენტების ჯგუფებისთვის. ეს სექცია ძირითადად გამოიყენება სტილიზაციის ან JavaScript-ის მიზნებისთვის და არა კონტენტის სტრუქტურირებისათვის.
#აქ შეიძლება იყოს:
#კონტეინერები, რომლებიც გაერთიანებენ სხვადასხვა ელემენტებს (მაგალითად, <header>, <footer>)
#ელემენტები, რომელთა გაერთიანება და მორგება ხდება CSS-ით



#footer სექცია ვებ-გვერდის ქვედა ნაწილში განთავსდება და ხშირად შეიცავს პატენტის ან ავტორის ინფორმაციას, კოპირაბის ან სხვა დამხმარე ელემენტებს.
#აქ შეიძლება იყოს:
#ავტორის ინფორმაცია (<p>, <address>)
#ლინკები ან ინფორმაციული წყაროები


#aside სექცია წარმოადგენს გვერდის მთავარ შინაარსთან დაკავშირებულ ან გვერდით მდგომ დამატებითი ინფორმაციის ნაწილს.
#ეს შეიძლება იყოს:
#გვერდითი ბარი, სადაც განთავსდება დამატებითი ინფორმაცია (მაგალითად, რეკლამები, რჩევები)
#კონტენტი, რომელიც არ არის პირდაპირ დაკავშირებული მთავარ შინაარსთან, მაგრამ შეიძლება იყოს საინტერესო მომხმარებლებისთვის




#aside სექცია წარმოადგენს გვერდის მთავარ შინაარსთან დაკავშირებულ ან გვერდით მდგომ დამატებითი ინფორმაციის ნაწილს.
#ეს შეიძლება იყოს:
#გვერდითი ბარი, სადაც განთავსდება დამატებითი ინფორმაცია (მაგალითად, რეკლამები, რჩევები)
#კონტენტი, რომელიც არ არის პირდაპირ დაკავშირებული მთავარ შინაარსთან, მაგრამ შეიძლება იყოს საინტერესო მომხმარებლებისთვის